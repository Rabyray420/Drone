//Константы для валидации:
const form = document.forms["feedback-form"];
const formArr = Array.from(form);
const validFormArr = [];
const button = form.elements["feedback-form-button"];
//Константы для счётчика:
const textArea = document.querySelector(".feedback-form__textarea");
const counter = document.querySelector(".counter-text__current");

//Счётчик символов:
textArea.addEventListener("input", onInput);
function onInput(evt) {
  const length = evt.target.value.length;
  counter.innerText = length;
}

formArr.forEach((el) => {
  if (el.hasAttribute("data-reg")) {
    el.setAttribute("is-valid", "0");
    validFormArr.push(el);
  }
});

//Дальше валидация:
form.addEventListener("input", inputHandler);
button.addEventListener("click", buttonHandler);

function inputHandler({ target }) {
  if (target.hasAttribute("data-reg")) {
    inputCheck(target);
  }
}

function inputCheck(el) {
  const inputValue = el.value;
  const inputReg = el.getAttribute("data-reg");
  const reg = new RegExp(inputReg);

  if (reg.test(inputValue)) {
    el.setAttribute("is-valid", "1");
    el.style.border = "2px solid rgb(0, 196, 0)";
  } else {
    el.setAttribute("is-valid", "0");
    el.style.border = "2px solid rgb(255, 0, 0)";
  }
}

